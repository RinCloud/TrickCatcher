#include <iostream>
#include <string>

int main() {
    int N;
    std::cin >> N;

    std::string S, T;
    std::cin >> S >> T;

    std::string newString;
    for (int i = 0; i < N; i++) {
        newString += S[i];
        newString += T[i];
    }

    std::cout << newString << std::endl;

    return 0;
}