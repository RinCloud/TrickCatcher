#include <iostream>

int main() {
    int A, B;
    std::cin >> A >> B;
    
    if(A == B) {
        std::cout << "Draw" << std::endl;
    } else if(A == 1) {
        std::cout << "Alice" << std::endl;
    } else if(B == 1) {
        std::cout << "Bob" << std::endl;
    } else if(A > B) {
        std::cout << "Alice" << std::endl;
    } else {
        std::cout << "Bob" << std::endl;
    }
    
    return 0;
}