#include <iostream>

int main() {
    char a, b;
    std::cin >> a >> b;
    
    if (a == 'H' && b == 'H') {
        std::cout << "H" << std::endl;
    } else if (a == 'D' && b == 'H') {
        std::cout << "D" << std::endl;
    } else if (a == 'H' && b == 'D') {
        std::cout << "D" << std::endl;
    } else if (a == 'D' && b == 'D') {
        std::cout << "H" << std::endl;
    }
    
    return 0;
}