#include <iostream>
#include <string>

int main() {
    int a, b;
    std::cin >> a >> b;
    
    std::string strA, strB;
    for (int i = 0; i < b; i++) {
        strA += std::to_string(a);
    }
    
    for (int i = 0; i < a; i++) {
        strB += std::to_string(b);
    }
    
    if (strA < strB) {
        std::cout << strA;
    } else {
        std::cout << strB;
    }
    
    return 0;
}