#include <iostream>
#include <string>

int main() {
    std::string S;
    std::cin >> S;

    int mind = 0;
    for (int i = 0; i < S.size(); i++) {
        if (S[i] == '+') {
            mind++;
        } else {
            mind--;
        }
    }

    std::cout << mind << std::endl;

    return 0;
}