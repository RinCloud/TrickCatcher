#include <iostream>
#include <string>

int main() {
    std::string symbols;
    std::cin >> symbols;

    int count = 0;
    for (char symbol : symbols) {
        if (symbol == '+') {
            count++;
        } else if (symbol == '-') {
            count--;
        }
    }
    
    std::cout << count << std::endl;

    return 0;
}