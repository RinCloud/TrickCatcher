#include <iostream>
#include <string>
using namespace std;

int main() {
    string N;
    cin >> N;

    int sumOfDigits = 0;
    for (char digit : N) {
        sumOfDigits += digit - '0';
    }

    if (sumOfDigits % 9 == 0) {
        cout << "Yes" << endl;
    } else {
        cout << "No" << endl;
    }

    return 0;
}