#include <iostream>
#include <cmath>

int main() {
    double A;
    double B;
    std::cin >> A >> B;
    
    // Multiply A and B
    double result = A * B;
    
    // Truncate the fractional part
    int integerResult = std::trunc(result);
    
    // Print the result
    std::cout << integerResult << std::endl;

    return 0;
}