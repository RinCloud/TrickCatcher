def count_verdicts(N, verdicts):
    AC_count = verdicts.count('AC')
    WA_count = verdicts.count('WA')
    TLE_count = verdicts.count('TLE')
    RE_count = verdicts.count('RE')

    print(f'AC x {AC_count}')
    print(f'WA x {WA_count}')
    print(f'TLE x {TLE_count}')
    print(f'RE x {RE_count}')


N = int(input())
verdicts = []
for _ in range(N):
    verdict = input()
    verdicts.append(verdict)

count_verdicts(N, verdicts)