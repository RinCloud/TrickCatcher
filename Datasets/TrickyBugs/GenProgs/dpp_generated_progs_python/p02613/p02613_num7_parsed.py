# Program:

N = int(input())
verdicts = []
for _ in range(N):
    verdicts.append(input())

counts = {"AC": 0, "WA": 0, "TLE": 0, "RE": 0}
for verdict in verdicts:
    counts[verdict] += 1

print(f"AC x {counts['AC']}")
print(f"WA x {counts['WA']}")
print(f"TLE x {counts['TLE']}")
print(f"RE x {counts['RE']}")