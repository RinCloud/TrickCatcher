a, b, c = map(int, input().split())

# Check if the total number of candies is divisible by 2
if (a + b + c) % 2 == 0:
    # Check if the maximum number of candies is less than or equal to half of the total number of candies
    if max(a, b, c) <= (a + b + c) / 2:
        print('Yes')
    else:
        print('No')
else:
    print('No')