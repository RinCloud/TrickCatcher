c = []

for _ in range(3):
    c.append(list(map(int, input().split())))

a1 = c[0][0] - 0
a2 = c[1][0] - 0
a3 = c[2][0] - 0

b1 = c[0][0] - a1
b2 = c[0][1] - a1
b3 = c[0][2] - a1

if a2 + b2 != c[1][1] or a2 + b3 != c[1][2] or a3 + b2 != c[2][1] or a3 + b3 != c[2][2]:
    print("No")
else:
    print("Yes")