# Given inputs
X, Y = map(int, input().split())

# Initialize counter and current number
count = 0
curr_num = X

# While the next number is less than or equal to Y
while curr_num <= Y:
    # Increment count for each valid number
    count += 1
    # Multiply current number by 2 for next iteration
    curr_num *= 2
    
# Print the maximum possible length of the sequence
print(count)