# Function to find the maximum possible length of the integer sequence
def find_max_sequence_length(X, Y):
    count = 0
    current = X
    while current <= Y:
        count += 1
        current *= 2
    return count

# Read the input values
X, Y = map(int, input().split())

# Find and print the maximum possible length of the sequence
print(find_max_sequence_length(X, Y))