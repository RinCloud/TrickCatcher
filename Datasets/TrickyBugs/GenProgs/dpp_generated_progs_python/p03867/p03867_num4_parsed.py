# Start of program
MOD = 10 ** 9 + 7

# Read input
N, K = map(int, input().split())

# Initialize count
count = 0

# Iterate over all possible values of a
for a in range(1, K + 1):

    # Check if a is a palindrome
    if str(a) == str(a)[::-1]:

        # Increment count
        count += 1

# Calculate result
result = pow(count, N, MOD)

# Print result
print(result)
# End of program