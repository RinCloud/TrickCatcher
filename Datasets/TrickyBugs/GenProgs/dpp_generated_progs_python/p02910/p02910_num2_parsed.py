# Program

S = input()

odd_chars = ['R', 'U', 'D']
even_chars = ['L', 'U', 'D']

for i in range(len(S)):
    if (i+1) % 2 == 1:
        if S[i] not in odd_chars:
            print('No')
            break
    else:
        if S[i] not in even_chars:
            print('No')
            break
else:
    print('Yes')