# Get the input values of A and B
A, B = map(int, input().split())

# Initialize a counter for palindromic numbers
count = 0

# Iterate through each number between A and B
for num in range(A, B+1):
    # Check if the number is palindromic
    if str(num) == str(num)[::-1]:
        # If the number is palindromic, increment the counter
        count += 1

# Print the count of palindromic numbers
print(count)