# Given three tasks A_1, A_2, A_3
# Find the minimum total cost required to complete all the tasks

# Read the input tasks
A_1, A_2, A_3 = map(int, input().split())

# Sort the tasks in ascending order
tasks = [A_1, A_2, A_3]
tasks.sort()

# Calculate the minimum total cost
total_cost = tasks[1] - tasks[0] + tasks[2] - tasks[1]

# Print the minimum total cost
print(total_cost)