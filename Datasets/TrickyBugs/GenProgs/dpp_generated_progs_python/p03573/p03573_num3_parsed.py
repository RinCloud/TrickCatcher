# Given three integers A, B, and C, among which two are the same and one is different, find the integer that is different from the rest.

# Read the inputs
A, B, C = map(int, input().split())

# Check if A is different from B and C
if A != B and A != C:
    print(A)
# Check if B is different from A and C
elif B != A and B != C:
    print(B)
# The remaining integer is C
else:
    print(C)