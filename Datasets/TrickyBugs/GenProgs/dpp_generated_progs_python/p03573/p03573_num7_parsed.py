# Given three integers A, B, and C
# Two of them are the same, while the remaining one is different
# Find the integer that is different from the rest

# Read the input values for A, B, and C
A, B, C = map(int, input().split())

# Check if A is different from B and C
if A != B and A != C:
    # A is different, so print A
    print(A)
# Check if B is different from A and C
elif B != A and B != C:
    # B is different, so print B
    print(B)
# If A and B are same, C is different
else:
    # C is different, so print C
    print(C)