import math

def choose_even_odd_pairs(K):
    even_count = math.ceil(K / 2) # number of even numbers between 1 and K
    odd_count = K // 2 # number of odd numbers between 1 and K
    total_pairs = even_count * odd_count
    return total_pairs

K = int(input())

pair_count = choose_even_odd_pairs(K)
print(pair_count)