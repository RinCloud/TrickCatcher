import math

K = int(input())

# Calculate the number of even numbers and odd numbers between 1 and K
even_numbers = math.floor(K/2)  # divide K by 2 and round down to the nearest integer
odd_numbers = math.ceil(K/2)  # divide K by 2 and round up to the nearest integer

# Calculate the number of ways to choose a pair of an even number and an odd number
num_ways = even_numbers * odd_numbers

print(num_ways)