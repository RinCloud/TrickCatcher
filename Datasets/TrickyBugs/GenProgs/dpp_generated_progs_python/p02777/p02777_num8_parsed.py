# Program

def count_balls(S, T, A, B, U):
    if U == S:
        return str(A-1) + " " + str(B)
    else:
        return str(A) + " " + str(B-1)

# Getting input
S, T = input().split()
A, B = map(int, input().split())
U = input()

# Calling the function and printing the result
print(count_balls(S, T, A, B, U))