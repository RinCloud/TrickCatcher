# Program to determine if there is a way to buy cakes and doughnuts for a given amount

N = int(input())

# Determine if there is a way to buy some cakes and some doughnuts for exactly N dollars
if N % 4 == 0 or N % 7 == 0 or N % 11 == 0 or N % 15 == 0 or N % 19 == 0 or N % 23 == 0 or N % 27 == 0 or N % 31 == 0 or N % 35 == 0:
    print("Yes")
else:
    print("No")