import sys

def main():
    # Read input
    n = int(sys.stdin.readline().strip())
    s, t = sys.stdin.readline().strip().split()

    # Form the new string by alternating characters
    new_string = ""
    for i in range(n):
        new_string += s[i] + t[i]

    # Print the new string
    print(new_string)

if __name__ == "__main__":
    main()