# Given the values of A, B, and C, we need to determine the maximum number of times Takahashi can hear the sound.

# Read the input values
A, B, C = map(int, input().split())

# Determine the maximum number of times the sound can be heard
if B // A <= C:
    # If the number of times the sound can be heard using all available money is less than or equal to C
    # Then output that number of times
    print(B // A)
else:
    # Otherwise, output C indicating that the sound can be heard maximum C times
    print(C)